                ps.setString(15, dob);
                ps.setInt(3, age1);
                ps.setString(6, LDrug);
                ps.setString(7, Lcomplain);
                ps.setString(8, Alias);
                ps.setString(13, addmision);
                ps.setString(14, discharge);
                ps.setString(9, address);
                ps.setString(10, Ldiagnose);
                fileInputStream.read(b);
                fileInputStream.close();
                ps.setBytes(11, b);

                //radiobutton1
                if (jRadioButton1.isSelected()) {
                    rBtn = jRadioButton1.getText();
                    ps.setString(5, rBtn);
                }
                //radiobutton2
                if (jRadioButton2.isSelected()) {
                    rBtn = jRadioButton2.getText();
                    ps.setString(5, rBtn);
                }
                //to get the row number thats why the int is there it do the upadate
                int val = ps.executeUpdate();
