    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        try {
            String str = jTextField1.getText();
            ps = connection.prepareStatement("select * from patientdetails where hospitalid = ?");
            ps.setString(1, str);
            rs = ps.executeQuery();
            if (rs.next()) {
                String firstNa = rs.getString("FIRSNAME");
                String LastNa = rs.getString("LASTNAME");
                String age = rs.getString("Age");
                String gender = rs.getString("Gender");
                String Ldrug = rs.getString("LDRUG");
                String Lcomplain = rs.getString("Lcomplain");
                String Alias = rs.getString("Alias");
                String adress = rs.getString("Adresss");
                String LDiagnose = rs.getString("Ldiagnose");
                String HopotalId = rs.getString("Hospitalid");
                byte[] profile = rs.getBytes("Profile");

                String admins = rs.getString("Addmissiondate");
                String dis = rs.getString("Discharge");
                String dob = rs.getString("DOB");
                //setting records
                FnTextField1.setText(firstNa);
                jTextField2.setText(LastNa);
                jTextField3.setText(dob);//date of b
                jTextField4.setText(age);
                // jRadioButton1.setText(gender);
                if (gender.equals("male")) {
                    jRadioButton1.doClick();
                } else {
                    jRadioButton2.doClick();
                }
                jTextField11.setText(Ldrug);
                jTextArea1.setText(Lcomplain);
                jTextArea2.setText(LDiagnose);
                jLabel2.setIcon(new ImageIcon(Toolkit.getDefaultToolkit().createImage(profile)));
                jTextField6.setText(Alias);
                jTextField7.setText(admins);
                jTextField8.setText(dis);
                jTextField9.setText(adress);
                jTextField10.setText(HopotalId);
            } else {
                JOptionPane.showMessageDialog(PDetails.this, "SORRY NO SUCH RECORD");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(PDetails.this, ex);
        }

    }//GEN-LAST:event_jButton7ActionPerformed

    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
        // TODO add your handling code here:

        try {

            ps = connection.prepareStatement("update patientdetails set lcomplain=?,ldiagnose=?,lDrug=? where hospitalid =?");
            String hoid = jTextField1.getText();
            String lcom = jTextArea1.getText();
            String ldia = jTextArea2.getText();
            String ldrug=jTextField11.getText();
            if (lcom.length() == 0 && ldia.length() == 0) {
                JOptionPane.showMessageDialog(PDetails.this, "Records can not be empty");
            } else if (hoid.length() == 0) {
                JOptionPane.showMessageDialog(PDetails.this, "Please enter the Hospital-Id");
            } else {
                ps.setString(1, lcom);
                ps.setString(2, ldia);
                ps.setString(3, hoid);
                ps.setString(4, ldrug);

                ps.executeUpdate();
                JOptionPane.showMessageDialog(PDetails.this, "Updated");
            }
        } catch (SQLException sqe) {
            JOptionPane.showMessageDialog(PDetails.this, sqe);
        }

    }//GEN-LAST:event_jButton9ActionPerformed

